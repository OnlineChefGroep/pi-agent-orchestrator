#!/usr/bin/env node
/**
 * tmux scene choreography for showcase recording.
 *
 * Generates a bash script that drives a tmux session running pi CLI
 * through all showcase scenes with SMOOTH, CALM pacing.
 *
 * Transitions use real ANSI escape codes (dim → clear → reset) sent
 * via tmux send-keys, with Escape-first to close any open overlays.
 *
 * Usage: node showcase-tmux-scenes.mjs --session <name>
 * Output: bash script to stdout
 */

const args = process.argv.slice(2)
function flag (name, fallback) {
  const i = args.indexOf(`--${name}`)
  return i !== -1 && args[i + 1] ? args[i + 1] : fallback
}

const SESSION = flag('session', 'showcase-tmux')

// ── helpers ──
const lines = []
const out = (s) => lines.push(s)

function sleep (ms) {
  out(`sleep ${(ms / 1000).toFixed(3)}`)
}

function tmuxKey (key) {
  out(`tmux send-keys -t "${SESSION}" "${key}"`)
}

function tmuxChar (ch) {
  const escaped = ch.replace(/\\/g, '\\\\').replace(/"/g, '\\"')
  out(`tmux send-keys -t "${SESSION}" -l "${escaped}"`)
}

function tmuxEnter () {
  out(`tmux send-keys -t "${SESSION}" Enter`)
}

function typeCharacterByCharacter (text, charDelayMs = 80) {
  for (const ch of text) {
    tmuxChar(ch)
    sleep(charDelayMs)
  }
}

function typeCmdNatural (cmd, waitAfterMs = 1200) {
  out(`# type: ${cmd}`)
  typeCharacterByCharacter(cmd, 80)
  sleep(200)
  tmuxEnter()
  sleep(waitAfterMs)
  out('')
}

function navigateSlow (key, count, delayMs = 400) {
  for (let i = 0; i < count; i++) {
    tmuxKey(key)
    sleep(delayMs)
  }
}

/**
 * Smooth scene transition with ANSI crossfade:
 *   1. Escape to close any overlay
 *   2. Dim text (CSI 2m) — visual fade-down
 *   3. Clear screen (CSI 2J CSI H) — black moment
 *   4. Reset attributes (CSI 0m) — bright again
 */
function sceneTransition (label, pauseMs = 1500) {
  out(`# ── TRANSITION: ${label} ──`)
  tmuxKey('Escape')
  sleep(300)
  out(`tmux send-keys -t "${SESSION}" $'\\x1b[2m'`)
  sleep(400)
  out(`tmux send-keys -t "${SESSION}" $'\\x1b[2J\\x1b[H'`)
  sleep(300)
  out(`tmux send-keys -t "${SESSION}" $'\\x1b[0m'`)
  sleep(pauseMs)
  out('')
}

// ══════════════════════════════════════════════════════════
// BASH HEADER
// ══════════════════════════════════════════════════════════
out('#!/usr/bin/env bash')
out('# Auto-generated by showcase-tmux-scenes.mjs — do not edit manually')
out('# Pacing: CALM — ANSI crossfade transitions, character-by-character typing')
out('set -euo pipefail')
out(`SESSION="${SESSION}"`)
out('')

// ══════════════════════════════════════════════════════════
// Scene 1: Dashboard overview
// ══════════════════════════════════════════════════════════
out('# ═══ Scene 1: Dashboard overview ═══')
sleep(3000)

typeCmdNatural('/agents', 2000)
sleep(2500)

out('# Slowly scroll through agents')
navigateSlow('j', 1, 900)
sleep(1500)
navigateSlow('j', 1, 900)
sleep(1500)
navigateSlow('j', 1, 900)
sleep(2000)

navigateSlow('k', 3, 900)
sleep(1500)

// ══════════════════════════════════════════════════════════
// Scene 2: Help overlay
// ══════════════════════════════════════════════════════════
sceneTransition('dashboard → help', 1200)
out('# ═══ Scene 2: Help overlay ═══')

typeCmdNatural('?', 2500)
sleep(2000)

tmuxKey('Escape')
sleep(1500)

// ══════════════════════════════════════════════════════════
// Scene 3: Top view — resource usage
// ══════════════════════════════════════════════════════════
sceneTransition('help → top view', 1200)
out('# ═══ Scene 3: Top view ═══')

typeCmdNatural('t', 2500)
sleep(2000)

out('# Sort by tokens')
tmuxKey('t')
sleep(2500)

out('# Sort by last seen')
tmuxKey('l')
sleep(2500)

out('# Sort by tool uses')
tmuxKey('u')
sleep(2500)

out('# Back to dashboard')
tmuxKey('t')
sleep(1500)

// ══════════════════════════════════════════════════════════
// Scene 4: Widget heatmap
// ══════════════════════════════════════════════════════════
sceneTransition('top view → widget', 1200)
out('# ═══ Scene 4: Widget heatmap ═══')

typeCmdNatural('w', 2500)

out('# Let widget animate')
sleep(4000)
sleep(2000)

tmuxKey('Escape')
sleep(1500)

// ══════════════════════════════════════════════════════════
// Scene 5: Agent spawn flow
// ══════════════════════════════════════════════════════════
sceneTransition('widget → agent spawn', 1200)
out('# ═══ Scene 5: Agent spawn flow ═══')

out('# Navigate to Create new agent')
navigateSlow('Down', 2, 600)
sleep(800)
tmuxKey('Enter')
sleep(1500)

out('# Select Explore agent type')
tmuxKey('Down')
sleep(800)
tmuxKey('Enter')
sleep(1500)

out('# Type agent description (character by character)')
typeCharacterByCharacter('Trace RPC handlers and document findings', 70)
sleep(400)
tmuxEnter()
sleep(3000)

out('# Let agent spawn')
sleep(3000)

out('# Return to dashboard')
tmuxKey('Escape')
sleep(1000)
tmuxKey('Escape')
sleep(1500)

// ══════════════════════════════════════════════════════════
// Scene 6: Settings menu
// ══════════════════════════════════════════════════════════
sceneTransition('agent spawn → settings', 1200)
out('# ═══ Scene 6: Settings menu ═══')

out('# Navigate to Settings')
navigateSlow('Down', 4, 600)
sleep(800)
tmuxKey('Enter')
sleep(1500)

sleep(1500)

out('# Navigate to Prompt compression')
navigateSlow('Down', 10, 200)
sleep(1000)
tmuxKey('Enter')
sleep(1200)

out('# Select balanced compression')
tmuxKey('Down')
sleep(600)
tmuxKey('Enter')
sleep(1000)

tmuxKey('Escape')
sleep(1000)
tmuxKey('Escape')
sleep(1500)

// ══════════════════════════════════════════════════════════
// Scene 7: Swarm topology
// ══════════════════════════════════════════════════════════
sceneTransition('settings → swarm', 1200)
out('# ═══ Scene 7: Swarm topology ═══')

typeCmdNatural('/agents', 2000)

out('# Navigate to swarm section')
navigateSlow('j', 3, 700)
sleep(2000)

// ══════════════════════════════════════════════════════════
// Final: Clean dashboard view
// ══════════════════════════════════════════════════════════
sceneTransition('swarm → final', 1500)
out('# ═══ Final: clean dashboard ═══')

tmuxKey('g')
sleep(3000)

out('# Final breathing room')
sleep(2000)

out(`tmux send-keys -t "${SESSION}" "Escape" 2>/dev/null || true`)

process.stdout.write(`${lines.join('\n')}\n`)
